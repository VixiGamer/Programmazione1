package calcolatrice;

public class Calcolatrice {
	
	static int Somma(int n1, int n2) {
		return n1 + n2;
	}
	
	static int Sottrazione(int n1, int n2) {
		return n1 - n2;
	}
	
	static int Moltiplicazione(int n1, int n2) {
		return n1 * n2;
	}
	
	static int Divisione(int n1, int n2) {
		return n1 / n2;
	}
}
